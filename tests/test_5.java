
class increement_decrement {
    int k, a;
    double d, g;
    public void main(){
        k = 7;
        g = 2.33 - 1;
        a = 8 * 1;
        d = 5 + 6.556d;
        k++;
        g--;
        --a;
        d++;
        for(int j = 1; j < a; j = j + 2)
        {
            g--;
        }
        g = g - 1;
    }
  }